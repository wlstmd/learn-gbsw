#include<stdio.h>
int A(int a){
    if(a<=1) return a;
    else    return A(a-1)+A(a-2);
}
int main(){
    int a=10;
    printf("%d",A(a));
}

// A(0) = 0
// A(1) = 1
// A(2) = A(1) + A(0) = 1 + 0 = 1
// A(3) = A(2) + A(1) = 1 + 1 = 2
// A(4) = A(3) + A(2) = 2 + 1 = 3
// A(5) = A(4) + A(3) = 3 + 2 = 5
// A(6) = A(5) + A(4) = 5 + 3 = 8
// A(7) = A(6) + A(5) = 8 + 5 = 13
// A(8) = A(7) + A(6) = 13 + 8 = 21
// A(9) = A(8) + A(7) = 21 + 13 = 34
