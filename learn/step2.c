#include<stdio.h>
int A(int a){
    if(a<=1) return a;
    else return a+A(a-1);
}
int main(){
    int a=10;
    printf("%d",A(a));
}

// A(0) = 0
// A(1) = 1
// A(2) = 2 + A(1) = 2 + 1 = 3
// A(3) = 3 + A(2) = 3 + 3 = 6
// A(4) = 4 + A(3) = 4 + 6 = 10
// A(5) = 5 + A(4) = 5 + 10 = 15
// A(6) = 6 + A(5) = 6 + 15 = 21
// A(7) = 7 + A(6) = 7 + 21 = 28
// A(8) = 8 + A(7) = 8 + 28 = 36
// A(9) = 9 + A(8) = 9 + 36 = 45
